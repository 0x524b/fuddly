PRAGMA foreign_keys = off;
BEGIN TRANSACTION;

CREATE TABLE CONF (
    ITEM  TEXT    PRIMARY KEY
                  UNIQUE
                  NOT NULL,
    VALUE BOOLEAN
);

CREATE TABLE DATAMODEL (
    DM_NAME TEXT PRIMARY KEY
               COLLATE NOCASE
               NOT NULL
               UNIQUE ON CONFLICT IGNORE
);

CREATE TABLE DMAKERS (
    DM_NAME           REFERENCES DATAMODEL (DM_NAME),
    NAME      TEXT,
    DISRUPTOR BOOLEAN,
    STATEFUL  BOOLEAN,
    PRIMARY KEY (
        DM_NAME,
        NAME
    )
);

CREATE TABLE DATA (
    ID        INTEGER  PRIMARY KEY ASC AUTOINCREMENT,
    TYPE      TEXT,
    DM_NAME   TEXT REFERENCES DATAMODEL (DM_NAME),
    CONTENTS  BLOB,
    SIZE      INTEGER,
    SENT_DATE DATETIME,
    ACK_DATE  DATETIME
);

CREATE TABLE STEPS (
    DATA_ID   INTEGER REFERENCES DATA (ID),
    STEP_ID   INTEGER,
    DMAKER_NAME         REFERENCES DMAKERS (NAME),
    GEN_ARGS  TEXT,
    SPE_ARGS  TEXT,
    PRIMARY KEY (
        DATA_ID,
        STEP_ID
    )
);

CREATE TABLE FEEDBACK (
    DATA_ID  INTEGER REFERENCES DATA (ID),
    FBK_ID   INTEGER,
    SRC      TEXT,
    CONTENTS BLOB,
    PRIMARY KEY (
        DATA_ID,
        FBK_ID
    )
);

COMMIT TRANSACTION;
PRAGMA foreign_keys = on;
